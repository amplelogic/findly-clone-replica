const Index = () => {
  const [tools, setTools] = useState<any[]>([]);
  const [user, setUser] = useState<any>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    checkAuth();
    fetchTools();
  }, []);

  const checkAuth = async () => {
    const { data: { session } } = await supabase.auth.getSession();
    setUser(session?.user ?? null);
  };

  const fetchTools = async () => {
    const { data, error } = await supabase
      .from("tools")
      .select("*")
      .order("created_at", { ascending: false });

    if (!error && data) {
      setTools(data);
    }
    setLoading(false);
  };

  return (
    <SidebarProvider>
      <div className="flex min-h-screen w-full bg-background">
        <CategorySidebar />
        
        <main className="flex-1 w-full">
          <header className="border-b border-border bg-card sticky top-0 z-10">
            <div className="container mx-auto px-6 py-3">
              <div className="flex items-center justify-between">
                <div className="flex items-center gap-3">
                  <SidebarTrigger className="lg:hidden" />
                  <div className="bg-foreground text-background px-2.5 py-1 rounded font-bold text-xs">
                    FINDLY.TOOLS
                  </div>
                  <div className="hidden sm:flex items-center gap-2 px-2.5 py-1 bg-accent/10 rounded text-xs font-medium text-accent-foreground">
                    <div className="w-1.5 h-1.5 rounded-full bg-accent"></div>
                    CERTIFIED DOMAIN RATING
                  </div>
                </div>
                {user ? (
                  <Link to="/admin">
                    <Button variant="ghost" className="text-xs">
                      Admin
                    </Button>
                  </Link>
                ) : (
                  <Link to="/auth">
                    <Button variant="ghost" className="text-xs">
                      Sign in
                    </Button>
                  </Link>
                )}
              </div>
            </div>
          </header>

          <section className="container mx-auto px-6 py-8">
            <h1 className="text-3xl font-bold text-foreground mb-1.5">
              The best tools, all in one place.
            </h1>
            <p className="text-sm text-muted-foreground mb-5">
              Find the best tools for you.
            </p>
            <Button className="bg-primary text-primary-foreground hover:bg-primary/90 text-sm px-4 py-2">
              <Plus className="h-4 w-4 mr-2" />
              Submit tool
            </Button>
          </section>

          <section className="container mx-auto px-4 sm:px-6 pb-12">
            {loading ? (
              <p>Loading tools...</p>
            ) : tools.length === 0 ? (
              <p className="text-muted-foreground">No tools found. Add some from the admin panel!</p>
            ) : (
              <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-2.5">
                {tools.map((tool) => (
                  <Link key={tool.id} to={`/tool/${tool.id}`}>
                    <ToolCard
                      name={tool.name}
                      description={tool.description}
                      logo={tool.logo}
                      badge={tool.badge as "New" | "Deal" | undefined}
                    />
                  </Link>
                ))}
              </div>
            )}
          </section>
        </main>
      </div>
    </SidebarProvider>
  );
};

export default Index;
